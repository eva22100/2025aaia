#include <stdio.h>
int main()
{
    int N=5;
    for(int i=0;i<N;i++){
        printf("%d ",i);
    }
    printf("\n");

    for(int i=1;i<N;i++){
        printf("%d ",i);
    }
    printf("\n");
    for(int i=1;i<N;i--){
        printf("%d ",i);
    }
    printf("\n");

}
